<h2>В моём детстве</h2>

Для меня эта тема началась относительно давно - в детстве, на даче. Тогда я наблюдал за разными приборами,
которыми занимался мой дед. То были сварочный аппарат, дрель, рубанок - много всего.<br/><br/>
Мне это нравилось и я тоже кое-что делал!<br/><br/>Например, собирал разные болты, гайки - скручивал их. Как-то даже сделал газонокосилку своими руками - деревянная рама,
листок ДВП снизу и нож, вращающийся на валу двигателя от стиральной машины. Руль от детского велосипеда трёхколёсного и от него же колёса. Косила машина за счёт электричества,
которое подавалось по проводу.<br/><br/>
А ещё мы с парнями ремонтировали и изменяли внешний вид своих велосипедов. Это было здорово!<br/> Был у нас такой буги-баги. Мы нашли раму от мопеда или мотоцикла.
Поставили на неё колёса от велосипеда и так вот катались, отталкиваясь ногами! Потом мой папа даже приварил нам сзади ось поперечную и "буги-баги" стал трёхколёсный.
Помню мы для взрослых сделали объявление и пытались его продать кому-нибудь. Это было замечательно!<br/><br/>
Помню ещё ребёнком около 4 лет я помогал маме на кухне и у неё сломалась чеснокодавка (выкрутился болт). Я взял похожий болт от железного конструктора (наверное, у многих
такие были) и починил. У меня даже не было идеи, сложно это или просто! Мне казалось это очевидным!<br/><br/>

<h2>Электрический ток</h2>

Потом в возрасте 5 лет (примерно) я возился с проводами, и решил собрать штекер для электрической сети. Я соединил два штырька вилки вместе и вставил в розетку! Меня тогда хорошо
тряхонуло, и я подумал, что меня бьёт мама, которая тогда была в коридоре, я же находился в комнате. Только позже мне объяснили, что это был электрический ток. И папа сказал - хорошо,
что он их соединил между собой!<br/><br/>
Недавно, когда я решил проверить работу обоих цилиндров на окушке, то сдёрнув высоковольтный провод я снова ощутил, что это такое - когда он прошёл через меня.
Сердце ещё некоторое время больно, а потом я его чувствовал некоторое время...

