<h2>Опыт с машинами</h2>

Первая машина у меня появилась весной 2012 года. И это была Mazda Demio на литье, с люком, праворульная механика 1.3 литра серого цвета.
Пробыла она у меня недолго, около двух месяцев...<br/><br/>Потом ещё были Pagero IO, отцовская шестёрка, Mark II 1989 года выпуска ярко-синего цвета.<br/>
И пожалуй всё...<br/><br/>Первая машина, в которой я сам что-то ремонтировал был Марк II. Но тут появилась она - жёлтая окушка! И моя жизнь изменилась...

