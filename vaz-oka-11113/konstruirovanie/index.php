<h2>Ока</h2>

Жизнь складывается по-разному. У меня ОКА и я радуюсь ей. Это ВАЗ 11113 2003 года - досталась она мне в октябре 2019 года, пробег 30000 с копейками. Третий хозяин.<br/>
<br/>Всё говорило о том, что пробег реальный - ни гнили на кузове, в салоне аккуратно. Недавно моё предположение подтвердилось, когда я был в шиномонтажке - шины родные с завода.<br/>
<br/>
И вот передо мной встали интересные задачи - от замены замка зажигания. А его я переделал на кнопку и на данный момент электрика с замка снята, но оставил я его для замыкания руля.
Также завод сделал с кнопки. Потом менял стартер - по воле случая предыдущий был испорчен (не мной). Потом разбирался с поврёждённой электрикой. Снимал панель...<br/>
<br/>Всё это пришлось делать, после того, как мальчишки по 17 лет угоняли машину. Полиция их нашла, ущёрб возместили - а я вот занялся восстановлением.<br/>
<br/>Также сам вставлял стёкла (4 из 6и) - мне девушка моя помогала, технология конечно очень интересная...<br/><br/>
Родное правое зеркало (со стороны пассажира) не встаёт в заводской комплектации - это описывают в интернете, не знаю, у всех ли так, но у меня так было.<br/>
<br/>Купив комплект евро-зеркал я сильно удивился, потому что правое зеркало также не удалось отрегулировать - регулировка даже в крайнем положении показывает водителю область,
слишком далеко от машины, как будто сделано так, чтобы пассажир в это зеркало смотрел.<br/>
<br/>И когда мне разбили евро-зеркала угонщики, то я воспользовался случаем и
старые родные зеркала (впрочем, одно оказывается было не родное, похоже - у родных больше железных элементов), а именно правое - разобрал, снял фиксатор, который используется 
для складывания зеркала и жидкими гвоздями зафиксировал его в нужном положении. Нужно было, чтобы прошло три дня, чтобы жидкие гвозди хорошо затвердели - и наконец, зеркало встало, как надо.

